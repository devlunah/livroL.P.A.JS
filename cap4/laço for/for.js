/*

Estrutura de Repetição FOR

Sintaxe:

for(var i = _; i _condicao_; i++/i--){
    comandos que irão ser executados várias vezes
}

Essa estrutura é composta de três instruções:

a) var i = _;

    Definição do valor inicial da variável de controle
    Sendo que:
        i corresponde a variável de controle

b) i _condicao_;

    Definição da condição que determina se a repetição deve ou não continuar

c) i++/i--

    Incremento ou decremento da variável de controle
    Sendo que:
        i++ ou i = i + 1 
            representa a incrementação da variável de controle
        i-- ou i = i - 1 
            representa a decrementação da variável de controle

*/