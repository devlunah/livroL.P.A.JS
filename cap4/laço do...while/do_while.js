/*

Estrutura de Repetição DO...WHILE

Sintaxe:

do {
    comandos;
} while (condicao);

A verificação da condição é feita no final da estrutura

 -> Fica garantido que uma vez, no mínimo, os comandos que pertencem ao laço serão executados

Esse laço de repetição geralmente é utilizado quando não soubermos previamente quantas vezes a repetição vai ocorrer

Importante: 
não esquecer de incrementar ou decrementar a varíavel de controle


*/ 