/*

Estrutura de Repetição WHILE

Sintaxe:

while (condicao){
    comandos;
}

Essa estrutura realiza um teste condicional logo no seu início, para verificar se os comandos do laço serão ou não executados

    "Enquanto a condição for verdadeira, execute"

Como o teste é realizado no início, é possível que os comandos dentro do while não sejam executados

Esse laço de repetição geralmente é utilizado quando não soubermos previamente quantas vezes a repetição vai ocorrer

Importante: 
não esquecer de incrementar ou decrementar a varíavel de controle


*/ 
